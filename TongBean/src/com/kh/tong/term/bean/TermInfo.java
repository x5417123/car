package com.kh.tong.term.bean;

public class TermInfo {
    String hwVerion;
    String swVersion;
    String osVersion;
    String sn;
    String password;
    String hotPassword;
    String hotName;
    String videoPassword;

    public String getHwVerion() {
        return hwVerion;
    }

    public void setHwVerion(String hwVerion) {
        this.hwVerion = hwVerion;
    }

    public String getSwVersion() {
        return swVersion;
    }

    public void setSwVersion(String swVersion) {
        this.swVersion = swVersion;
    }

    public String getOsVersion() {
        return osVersion;
    }

    public void setOsVersion(String osVersion) {
        this.osVersion = osVersion;
    }

    public String getSn() {
        return sn;
    }

    public void setSn(String sn) {
        this.sn = sn;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getHotPassword() {
        return hotPassword;
    }

    public void setHotPassword(String hotPassword) {
        this.hotPassword = hotPassword;
    }

    public String getHotName() {
        return hotName;
    }

    public void setHotName(String hotName) {
        this.hotName = hotName;
    }

    public String getVideoPassword() {
        return videoPassword;
    }

    public void setVideoPassword(String videoPassword) {
        this.videoPassword = videoPassword;
    }

}
